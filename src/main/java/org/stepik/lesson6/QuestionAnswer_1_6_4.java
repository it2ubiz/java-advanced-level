public class QuestionAnswer_1_6_4 {
    /*
     * Вопрос:
     * Select all conditions necessary to override a base class method in a subclass.
     *
     * Ответ:
     * A method in subclass must have the same returning type or a subclass of the returning type.
     * A method in subclass must have the same argument list as a method in base class.
     * A method in subclass must have not less access modifier as a method in base class.
     * A method in subclass must have the same name as a method in base class.
     * */
}
